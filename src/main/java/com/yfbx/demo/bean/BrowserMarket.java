package com.yfbx.demo.bean;

public class BrowserMarket {

    private int id;
    private String name;
    private float market_percent;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getMarket_percent() {
        return market_percent;
    }

    public void setMarket_percent(float market_percent) {
        this.market_percent = market_percent;
    }
}
